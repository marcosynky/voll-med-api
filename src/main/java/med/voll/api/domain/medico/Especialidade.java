package med.voll.api.domain.medico;

// O enum 'Especialidade' define as especialidades médicas que um médico pode ter
public enum Especialidade {

    ORTOPEDIA,     // Especialidade de ortopedia (tratamento de ossos, articulações e músculos)
    CARDIOLOGIA,   // Especialidade de cardiologia (tratamento de doenças do coração)
    GINECOLOGIA,   // Especialidade de ginecologia (cuidados da saúde feminina)
    DERMATOLOGIA;  // Especialidade de dermatologia (tratamento da pele)

}
